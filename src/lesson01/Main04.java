package lesson01;

public class Main04 {
    public static void main(String[] args) {
        int n = 5;
        System.out.println(n);
        n++; // n = n + 1
        System.out.println(n);
        // composite assignment operators
        n += 2; // n = n + 2
        System.out.println(n);
        n /= 4; // n = n / 4;
        System.out.println(n);
    }
}
