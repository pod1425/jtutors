package lesson04;

public class Main06 {
    public static void main(String[] args) {

        int counter = 1;
        while (counter <= 3) {
            System.out.println("Hello!");
            counter++;
        }

        System.out.println("--------");
        // for loop
        for (int i = 1; i <= 3; i++) {
            System.out.println("Hello!");
        }

    }
}
